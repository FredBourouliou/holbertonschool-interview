#ifndef HOLBERTON_H
#define HOLBERTON_H

/**
 * wildcmp - compares two strings, allowing '*' in s2 as a wildcard.
 * @s1: first string
 * @s2: second string, may contain the special character '*'
 *
 * Return: 1 if the strings can be considered identical, 0 otherwise.
 */
int wildcmp(char *s1, char *s2);

#endif /* HOLBERTON_H */
